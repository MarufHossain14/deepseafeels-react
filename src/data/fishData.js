export const getFishByMood = (mood) => {
  return (
    fishData.find((fish) =>
      fish.mood.toLowerCase().includes(mood.toLowerCase())
    ) || fishData[0]
  );
};

export const getRandomFish = () => {
  return fishData[Math.floor(Math.random() * fishData.length)];
};

export const calculateFishFromPoints = (points) => {
  const maxIndex = points.indexOf(Math.max(...points));
  return fishData[maxIndex];
};
